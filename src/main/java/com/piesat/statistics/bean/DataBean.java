package com.piesat.statistics.bean;

public class DataBean {
    private String data_code;
    private String data_name;
    private String data_class_id;

    private String data_class_name;

    public String getData_code() {
        return data_code;
    }

    public void setData_code(String data_code) {
        this.data_code = data_code;
    }

    public String getData_name() {
        return data_name;
    }

    public void setData_name(String data_name) {
        this.data_name = data_name;
    }

    public String getData_class_id() {
        return data_class_id;
    }

    public void setData_class_id(String data_class_id) {
        this.data_class_id = data_class_id;
    }

    public String getData_class_name() {
        return data_class_name;
    }

    public void setData_class_name(String data_class_name) {
        this.data_class_name = data_class_name;
    }
}
